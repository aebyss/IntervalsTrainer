package com.example.fretboardlearner.ui.theme

import androidx.compose.ui.graphics.Color

// Define colors for our TRUE OLED dark theme

// True black for OLED screens. The 0xFF prefix is for full alpha (non-transparent).
val OledBlack = Color(0xFF000000)

// We'll keep a very dark gray for surfaces. This creates a subtle, modern depth
// so that things like cards or dialogs can stand out slightly from the pure black background.
val DarkGraySurface = Color(0xFF121212)

val PrimaryText = Color(0xFFFFFFFF)     // White for primary text
val SecondaryText = Color(0xFFB0B0B0) // Light gray for secondary text
val AccentColor = Color(0xFF03DAC5)     // A teal/cyan accent for buttons